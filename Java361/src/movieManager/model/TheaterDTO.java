package movieManager.model;

public class TheaterDTO {
    
    private int theaterNumber;
    private String theaterName;
    private String theatherLocation;
    private String theaterPhone;
    
    
    public int getTheaterNumber() {
        return theaterNumber;
    }
    public void setTheaterNumber(int theaterNumber) {
        this.theaterNumber = theaterNumber;
    }
    public String getTheaterName() {
        return theaterName;
    }
    public void setTheaterName(String theaterName) {
        this.theaterName = theaterName;
    }
    public String getTheatherLocation() {
        return theatherLocation;
    }
    public void setTheatherLocation(String theatherLocation) {
        this.theatherLocation = theatherLocation;
    }
    public String getTheaterPhone() {
        return theaterPhone;
    }
    public void setTheaterPhone(String theaterPhone) {
        this.theaterPhone = theaterPhone;
    }
    
}
